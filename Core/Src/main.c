void SD_init(){
	// One line of LCD contains 21 character
	char buf[22];
	sprintf(buf, "Loading SD card...");
	LCD_FStr(buf, 0, 0);
	LCD_Update();

	// Wait 1 second before loading SD card
	HAL_Delay(1000);

	// Clear LCD before showing anything
	LCD_Clear();

	// Try to get SD card Sector Count
	sprintf(buf, "SD card sector count");
	LCD_FStr(buf, 0, 0);
	sprintf(buf, "%u", SD_GetSectorCount());
	LCD_FStr(buf, 0, 1);
	LCD_Update();
	HAL_Delay(2000);

	MX_FATFS_Init();
	exf_mount();
	exf_getfree();

	return;
};

void OLED_task(void * pvParameters) {
	char buf[22];
	while(1){
		// Clear OLED before displaying stuff
		LCD_Clear();
		sprintf(buf, "%s %c", "File Browser", 0x3C);
		LCD_FStr(buf, 0, 0);
		sprintf(buf, "%s", "Player");
		LCD_FStr(buf, 0, 1);
		sprintf(buf, "%s", "Setting");
		LCD_FStr(buf, 0, 2);
		sprintf(buf, "%s", "About");
		LCD_FStr(buf, 0, 3);
		LCD_Update();
		vTaskDelay(1000);
	}
	return;
};

void timer_task(void * pvParameters) {
	while(1){
		globalTime++;
		vTaskDelay(1000);
	}
	return;
}

